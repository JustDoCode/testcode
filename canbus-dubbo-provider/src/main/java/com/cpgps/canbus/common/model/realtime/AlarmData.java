public class AlarmData implements Serializable {
	private static final long serialVersionUID = 1L;
	private GpsLocation gpsLocation;
    private short alarmType;
    private short alarmDataLen;
    private AlarmIncidentalData alarmIncidentalData;
    public AlarmData(){
    }
    

    public GpsLocation getGpsLocation() {
        return gpsLocation;
    }

    public void setGpsLocation(GpsLocation gpsLocation) {
        this.gpsLocation = gpsLocation;
    }

    public short getAlarmType() {
        return alarmType;
    }

    public void setAlarmType(short alarmType) {
        this.alarmType = alarmType;
    }

    public short getAlarmDataLen() {
        return alarmDataLen;
    }

    public void setAlarmDataLen(short alarmDataLen) {
        this.alarmDataLen = alarmDataLen;
    }

    public AlarmIncidentalData getAlarmIncidentalData() {
        return alarmIncidentalData;
    }

    public void setAlarmIncidentalData(AlarmIncidentalData alarmIncidentalData) {
        this.alarmIncidentalData = alarmIncidentalData;
    }
}
